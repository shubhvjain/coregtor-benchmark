def get_data(input_details: dict, dataset: pd.DataFrame):
    """
    Prepare expression matrix for RTN/RTNduals.
    """
    tf_vec     = input_details["source_genes"]
    target_vec = input_details["target_genes"]
    
    genes = list(set(tf_vec + target_vec))
    
    subset = dataset[genes]
    subset = subset.loc[:, ~subset.columns.duplicated(keep='first')]
    # Assumes data requires log-transformation; skip if GCT is pre-logged
    matrix = np.log2(subset.values + 1)         
    matrix = matrix.T                            
    result = pd.DataFrame(matrix, index=genes, columns=dataset.index)    
    return result, tf_vec, target_vec

def get_max_job_memory_gb():
    slurm_mem_mb = os.environ.get('SLURM_MEM_PER_NODE')
    if slurm_mem_mb:
        return float(slurm_mem_mb) / 1024
    return None

def run_rtnduals(input_data, env):
    print("Initializing RTNduals Run...")
    #print(env)
    # 1. Fetch paths and setup data using your utilities
    out_path, temp_path = ut.get_exp_path(input_data, env)
    #print(out_path)
    i, d = ut.start_experiment(input_data, env)
    matrix_df, tf_vec, target_vec = get_data(i, d)
    
    # 2. Extract options safely with RTN-specific defaults
    opts = input_data.get("rtnduals_options", {})  
    # Loosen the initial constraints while keeping permutations high for statistical power
    n_permutations = opts.get("nPermutations", 2000)   # Boosts p-value resolution limit to 0.0005
    n_bootstrap = opts.get("nBootstrap", 100)
    p_adjust_method = opts.get("pAdjustMethod", "BH")
    p_value_cutoff = opts.get("pValueCutoff", 0.05)    # Kept at 0.05 for both steps to capture signal
    
    # Define isolated directory within /app/temp
    run_id = secrets.token_hex(4)
    r_worker_io_dir = temp_path / f"rtnduals_{run_id}"
    r_worker_io_dir.mkdir(parents=True, exist_ok=True)
    
    print(f"Staging temporary matrices in: {r_worker_io_dir}")
    
    # 3. Serialize data frames using Parquet
    matrix_df.to_parquet(r_worker_io_dir / "matrix.parquet", index=True)
    pd.DataFrame({"tfs": tf_vec}).to_parquet(r_worker_io_dir / "tfs.parquet", index=False)
    pd.DataFrame({"targets": target_vec}).to_parquet(r_worker_io_dir / "targets.parquet", index=False)

    # Save additional configurations as JSON
    options_payload = {
        "n_permutations": n_permutations,
        "n_bootstrap": n_bootstrap,
        "p_adjust_method": p_adjust_method,
        "p_value_cutoff": p_value_cutoff
    }
    with open(r_worker_io_dir / "options.json", "w") as f:
        json.dump(options_payload, f, indent=4)
    
    # 4. Construct Command arguments for Rscript
    cmd = [
        "Rscript",
        "/app/script/rtnduals.r",
        str(r_worker_io_dir)
    ]
    
    print(f"Executing worker command: {' '.join(cmd)}")

    log_file_path = out_path / "worker_run.log"
    print(f"R output is being redirected to: {log_file_path}")
        
    # 5. Run the subprocess
    with open(log_file_path, "w") as log_file:
        current_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        print(f"Started at : {current_time}", file=log_file)
        max_gb = get_max_job_memory_gb()
        if max_gb:
            print(f"Max memory available to this job: {max_gb:.2f} GB\n", file=log_file)

        result = subprocess.run(
            cmd, 
            stdout=log_file, 
            stderr=log_file, 
            text=True, 
            check=True
        )
    
    print("--- R Worker Output ---")
    print(result.stdout)
    print("-----------------------")
    
    # 6. Read back the results generated by R worker
    print("R execution complete. Importing results back into Python...")
    grn_df = pd.read_parquet(r_worker_io_dir / "rtnduals_output.parquet")
    
    cluster_file_name = f'{input_data.get("result_file_name","default")}.csv'
    cluster = format_rtnduals_results(input_data, grn_df)
    cluster.to_csv(out_path / cluster_file_name, index=False)
    cluster.to_csv(temp_path / cluster_file_name, index=False)
    
    import shutil
    shutil.rmtree(r_worker_io_dir)

def format_rtnduals_results(input_data, grn):
    """
    Formats the RTNduals TF-TF-Target interactions to match your exact output layout:
    A target gene mapped to its string of semicolon-separated coregulators.
    """
    method_label = f"exp-{input_data['id']}-tool-rtnduals"
    rows = []
    
    for target, group in grn.groupby("Target"):
        # Collect unique regulators (Regulator1 and Regulator2) for this target
        tfs = set(group["Regulator1"].tolist() + group["Regulator2"].tolist())
        sources = sorted(list(tfs))
        sources_str = ";".join(sources)
        n_sources = len(sources)

        rows.append({
            "uid": secrets.token_hex(6),
            "target":      target,
            "sources":     sources_str,
            "n_sources":   n_sources,
            "note":        method_label
        })

    return pd.DataFrame(rows)

def main():
    parser = argparse.ArgumentParser(description="To run RTNduals")
    parser.add_argument('--input', required=True, help="The path or identifier for the input data")
    args = parser.parse_args()
    try:
        env = ut.get_env()
        input_data = ut.get_input(args.input)
        run_rtnduals(input_data, env)
    except Exception as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()